class pattern11
{
	public static void main(String [] args)
	{
         int line=3;
         int star=4;
         for(int i=0; i<line; i++)
         {
         	char ch='A';
                for(int j=0; j<star; j++)
                	{
                             System.out.print(ch+" ");
                             ch++;
                        }
                        System.out.println();
         }
         
     }
}